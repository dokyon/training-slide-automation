例えば、しんじんのスタッフに「ちょっと、これお願い」とあいまいな指示を出したら、どうなるでしょうか？
おそらく、「えっと…具体的に何をすればいいですか？」と困った顔で聞き返してくるか、あるいは、けんとう違いの成果ぶつが出てきて、「うーん、そうじゃないんだよな…」と、やり直しをお願いすることになりますよね。

ChatGPTも、全く同じなんです。
「面白いブログ記事を書いて」とだけお願いしても、誰に向けた、どんなテーマの、どんな雰囲気の記事が欲しいのかが分からないので、ありきたりで、あなたの役には立たない文章しか出てきません。

