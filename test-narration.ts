ChatGPTとの付き合い方も、これと全く同じです。
私たちがこの講座で学んでいくのは、難しいプログラミングの知識ではありません。
この「賢いしんじんアシスタント」に対して、
・どうすれば、てきかくに指示が伝わるのか（＝じょうずな質問や命令のしかた）
・どうすれば、もっと質の高いアウトプットを出してくれるようになるのか（＝仕事の背景や条件の伝え方）
・どうすれば、あなたの仕事のスタイルを覚えてくれるのか（＝継続的なたいわによる育て方）
という、いわば「AIとのコミュニケーションじゅつ」なんです。

このスキルが身につくと、あなたの働き方は劇的に変わります。

これまであなたが３０分かけていたメールの作成は、たった３分で終わるかもしれません。
企画会議で頭を悩ませていた新しいアイデアの切り口を、アシスタントが１０個も２０個も提案してくれるようになります。
めんどうな情報収集やデータ整理は彼に任せて、あなたは、そのデータを見て「次に何をすべきか」という、人間にしかできない本質的な判断に時間を使えるようになります。

つまり、AIに「できること」を任せることで、私たちは、人間にしかできない「付加価値の高い仕事」や「創造的な仕事」に、もっと多くの時間とエネルギーをそそげるようになるのです。
これこそが、AI時代の新しい働き方であり、この講座を通して皆さんに手に入れてほしい未来の姿です。

それでは、今日のレッスンのまとめです。
大切なポイントは３つありました。

ひとつめ。この講座のゴールは、ChatGPTをあなただけの「賢いしんじんアシスタント」にそだてあげること。
