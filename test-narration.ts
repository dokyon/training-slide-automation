私たちが今どこにいて、これから何を学び、最終的にどこへたどり着くのか。次のレッスンでは、まずこの研修全体の「ロードマップ」を皆さんと共有します。最初に全体像をしっかり掴んでおくことで、一つ一つの学びが「使えるスキル」として繋がり、迷うことなくゴールにたどり着くことができますからね。
